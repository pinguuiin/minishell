#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	num;

	if (ft_strncmp(str, "-2147483648", 12) == 0)
		return (INT_MIN);
	sign = 1;
	num = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' || \
		*str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + (*str - '0');
		str++;
	}
	return (num * sign);
}
